async function executeGet(url,jsonState){
    //get síncrono com o uso do fetch
    await fetch(url)
    .then(response => {
          if (response.status === 200) {
            console.log('sucesso');
            response.json().then(function(result){ 

              //console.log(result);
              jsonState(result)

              });
          } else {
            throw new Error('Erro ao consumir a API!');
          }
      })
      .then(response => {
        //console.debug(response);
      }).catch(error => {
        console.error(error);
      });
  }



const ShowDetalhes = ({display,toogleModal,mensagem}) => (   
    <Modal
          animationType="slide"
          transparent={true}
          visible={display}
          onRequestClose={toogleModal}
    >

        <View style={styles.centeredView}>
          <View style={styles.modalView}>
                <Pressable onPress={toogleModal}>
                  <Text>{mensagem}</Text>
                </Pressable>
          </View>
        </View>
    
    </Modal>
        
 )

const Pessoa = ({name,email}) => {
    
    //state para controle do Modal
    const [modal,setModal] = React.useState(false)

    function mudaModal(){
      setModal(!modal)
    }

    return(
    <View>
      <ShowDetalhes display={modal} toogleModal={mudaModal} mensagem={email}/>
      
      <Pressable onPress={mudaModal}>
        <Text style={styles.paragraph}>{name}</Text>
      </Pressable>
    </View>
    )
}


export default function App() {

  const [jsonData,setJsonData] = React.useState({})

  executeGet("https://jsonplaceholder.typicode.com/users",setJsonData)

  //função que renderiza cada item do FlatList
  function meuItem({item}){
    let nomeCompleto = item.name
    
    return(
      <Pessoa name={item.name}
              email={item.email} 
      />
    )
  }
  function headerList (){
    return (
      <Text style={styles.header}>Lista de usuarios</Text>
    )
  }
    
  
  return (

    <View style={styles.container}>

        <FlatList
          data={jsonData}
          renderItem={meuItem}
          keyExtractor={item => item.id}
          ListHeaderComponent={headerList}
        />     
      
    </View>
  );
}

